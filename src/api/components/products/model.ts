import { model, Schema } from 'mongoose'

const productSchema = new Schema(
  {
    user: {
      type: Schema.Types.ObjectId,
      required: true,
      ref: 'User',
    },
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    brand: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      enum: ["ACTIVE", "NOT AVAILABLE"],
      default: "ACTIVE",
    },
  },
  {
    timestamps: true,
  }
);

const Product = model("Product", productSchema)

export default Product